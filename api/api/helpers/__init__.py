import datetime

def get_or_create(session, model, **kwargs):
    instance = session.query(model).filter_by(**kwargs).first()
    if instance:
        return instance
    else:
        instance = model(**kwargs)
        session.add(instance)
        session.commit()
        return instance
    
def dateSerializer(o):
    if isinstance(o, datetime.datetime):
        return o.__str__()